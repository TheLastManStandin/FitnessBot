        token = config.TELEGRAM_BOT_TOKEN

        if not token:
            raise ValueError("–û—à–∏–±–∫–∞: TELEGRAM_BOT_TOKEN –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω!")

        print(f"üîë –¢–æ–∫–µ–Ω –±–æ—Ç–∞ –ø–æ–ª—É—á–µ–Ω (–¥–ª–∏–Ω–∞: {len(token)} —Å–∏–º–≤–æ–ª–æ–≤)")

